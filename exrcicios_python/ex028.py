#Escreva um programa que faça o computador pensar em um número entre 0 e 5 e peça para o usuario tentar descobrir qual foi o  número escolhido pelo computador.#
import random
numero = int(input('Digite um número: '))
lista = [1,2,3,4,5]
sorteio = random.choice(lista)
print('O número sorteado foi {}'.format(sorteio))
print('ACERTOU' if numero == sorteio else "ERROU TENTE MAIS UMA VEZ")