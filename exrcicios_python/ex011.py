#Faça um programa que leia a largura e a altura de uma parede em metros. Calcule  a asua área e a quantidade de tinta necessária para pinta-la,sabendo que cada litro de tinta, pinta uma área de 2m quadrado#

altura = float(input('Informe a altura: '))
largura = float(input('Informe a largura:'))
litro_de_tinta = 2
area = altura*largura
Quant_D_tinta = area/litro_de_tinta
print('Você vai gastar {} litros de tinta para pintar sua parede10.'.format(Quant_D_tinta))

